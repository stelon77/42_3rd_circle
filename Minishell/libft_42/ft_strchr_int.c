#include "libft.h"

int	ft_strchr_int(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0' && s[i] != c)
	{
		i++;
	}
	if (s[i] == c)
	{
		return (1);
	}
	return (0);
}
